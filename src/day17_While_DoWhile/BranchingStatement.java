package day17_While_DoWhile;

public class BranchingStatement {
    public static void main(String[] args) {

        for (char i = 'A'; i <= 'Z' ; i++) {
            System.out.println(i+" ");
            if(i == 'F'){
                break;//Exist the loop
            }
        }
        System.out.println();
        System.out.println("-----------------------");
        



    }
}
